package wrapperclass;

public class Autounboxing {

	public static void main(String[] args) {
		Integer aobj=100;
		Character bobj='B';
		Double cobj=20.2;
		int a=aobj;
		char b=bobj;
		double c=cobj;
		System.out.println("Integer aobj="+aobj+"-> int a= "+a);
		System.out.println("Character aobj="+bobj+"-> char b= "+b);
		System.out.println("Double aobj="+cobj+"-> double c= "+c);
		

	}

}
